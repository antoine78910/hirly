export function getSwipeSuccessCopy(t, data, job) {
  const submission = data?.submission_status || "not_submitted";
  const pkg = data?.package_status || data?.application_status;
  const company = job?.company || "";

  if (data?.queued_for_generation || data?.generation_status === "pending_generation") {
    const queuedTitle = t("swipe.swipeQueued", { company });
    const queuedDesc = t("swipe.swipeQueuedDesc");
    return {
      title: queuedTitle === "swipe.swipeQueued" ? t("swipe.swipePackageFor", { company }) : queuedTitle,
      description: queuedDesc === "swipe.swipeQueuedDesc" ? t("swipe.swipePackageReady") : queuedDesc,
    };
  }

  if (data?.manual_fulfillment || data?.manual_status === "manual_review_needed") {
    return { title: t("swipe.swipePackageFor", { company }), description: t("swipe.swipePackageReady") };
  }

  if (submission === "prepared") {
    const reviewOn = readAiSettings().reviewDocuments;
    return {
      title: reviewOn ? t("swipe.swipeReadyReview") : t("swipe.swipeAppPrepared"),
      description: reviewOn
        ? t("swipe.swipeWaitingReview", { company })
        : t("swipe.swipeReadyApps", { company }),
    };
  }
  if (submission === "action_required" || submission === "blocked") {
    return { title: t("review.actionRequired"), description: t("swipe.swipeActionRequiredDesc") };
  }
  if (submission === "blocked_captcha") {
    return { title: t("swipe.swipeHumanVerification"), description: t("swipe.swipeHumanVerificationDesc") };
  }
  if (submission === "prepare_failed") {
    return { title: t("swipe.swipePackageGenerated"), description: t("swipe.swipePackageFailed") };
  }
  if (pkg === "generated_text_only") {
    return { title: t("swipe.swipeTextGenerated"), description: t("swipe.swipeTextGeneratedDesc") };
  }
  if (pkg === "needs_profile_data") {
    return { title: t("swipe.swipeMoreProfile"), description: t("swipe.swipeMoreProfileDesc") };
  }
  if (pkg === "needs_job_data") {
    return { title: t("swipe.swipeMoreJob"), description: t("swipe.swipeMoreJobDesc") };
  }
  if (pkg === "failed") {
    return { title: t("swipe.swipeAppSaved"), description: t("swipe.swipeAppSavedDesc") };
  }
  return {
    title: t("swipe.swipePackageFor", { company }),
    description: t("swipe.swipePackageReady"),
  };
}

export function getSwipeErrorMessage(t, e) {
  if (e?.code === "ECONNABORTED") return t("swipe.generationTimeout");
  const detail = e?.response?.data?.detail;
  if (typeof detail === "string") return detail;
  if (detail?.message) return detail.message;
  return t("toasts.swipeFailed");
}

export function getPackageErrorMessage(t, e) {
  const status = e?.response?.status;
  const detail = e?.response?.data?.detail;
  if (status === 402) return t("history.packageNoCredits");
  if (status === 404) {
    if (typeof detail === "string") return detail;
    return detail?.message || t("history.packageJobUnavailable");
  }
  if (status === 400) {
    const message = typeof detail === "string" ? detail : detail?.message;
    if (message && /profile/i.test(message)) return t("history.packageProfileRequired");
    if (message) return message;
  }
  if (status === 422) {
    const message = typeof detail === "object" ? detail?.message : detail;
    return message || t("history.packageJobNotApplyable");
  }
  return getSwipeErrorMessage(t, e) || t("history.packageError");
}

